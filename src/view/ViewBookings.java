    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    int row = tblAllBookings.getSelectedRow(); // Make sure this matches your table name
    
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a booking to cancel!");
        return;
    }

    try {
        // 1. Get the Date (Check if it is index 6!)
        String dateStr = tblAllBookings.getValueAt(row, 4).toString().trim();
        java.time.LocalDate travelDate = java.time.LocalDate.parse(dateStr);
        java.time.LocalDate today = java.time.LocalDate.now();

        // DEBUG POPUP: This will tell us if the dates are being read correctly
        // System.out.println("Comparing " + travelDate + " with " + today);

        // 2. THE BLOCKER
        if (travelDate.isBefore(today)) {
            JOptionPane.showMessageDialog(this, 
                "You cannot cancel a past booking.\nTrip Date: " + dateStr + "\nToday: " + today, 
                "Cancellation Denied", 
                JOptionPane.ERROR_MESSAGE);
            return; 
        }

        // 3. Normal logic only runs if date is Today or Future
        int confirm = JOptionPane.showConfirmDialog(this, "Cancel this ticket?", "Confirm", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            String bookingId = tblAllBookings.getValueAt(row, 0).toString();
            java.sql.Connection con = dao.ConnectionProvider.getCon();
            java.sql.PreparedStatement ps = con.prepareStatement("DELETE FROM bookings WHERE booking_id = ?");
            ps.setString(1, bookingId);
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Cancelled! Your seat is now available for others.");
            loadData();
            // refresh your table here, e.g., loadData();
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

