    private void btnViewTicketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewTicketActionPerformed
        int row = tblAllBookings.getSelectedRow();
    
    if(row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a booking from the table first!");
        return;
    }

    try {
        // SAFETY MATH: Check if the value is null before calling .toString()
        Object idVal = tblAllBookings.getValueAt(row, 0);
        Object passVal = tblAllBookings.getValueAt(row, 1);
        Object busVal = tblAllBookings.getValueAt(row, 2);
        Object routeVal = tblAllBookings.getValueAt(row, 3);
        Object dateVal = tblAllBookings.getValueAt(row, 4);
        Object amtVal = tblAllBookings.getValueAt(row, 5);

        // Convert to String safely
        String id = (idVal != null) ? idVal.toString() : "N/A";
        String passenger = (passVal != null) ? passVal.toString() : "N/A";
        String busNo = (busVal != null) ? busVal.toString() : "N/A";
        String route = (routeVal != null) ? routeVal.toString() : "N/A";
        String travelDate = (dateVal != null) ? dateVal.toString() : "N/A";
        String amount = (amtVal != null) ? amtVal.toString() : "0";

        // Format the Ticket
        String ticket = "------------------------------------------------------------\n"
                      + "             🚌 E-TICKET RECEIPT             \n"
                      + "------------------------------------------------------------\n"
                      + " Booking ID:      #" + id + "\n"
                      + " Passenger Name:  " + passenger + "\n"
                      + " Travel Date:     " + travelDate + "\n"
                      + "------------------------------------------------------------\n"
                      + " Bus Number:      " + busNo + "\n"
                      + " Bus Route:       " + route + "\n"
                      + "------------------------------------------------------------\n"
                      + " TOTAL PAID:      ₹" + amount + "\n"
                      + "------------------------------------------------------------\n"
                      + "        Thank you for traveling with us!            \n"
                      + "------------------------------------------------------------";

        // SHOW THE POPUP
        JOptionPane.showMessageDialog(this, ticket, "Ticket Details", JOptionPane.INFORMATION_MESSAGE);

        // Print choice
        int choice = JOptionPane.showConfirmDialog(this, "Do you want to save this as a .txt file?", "Print", JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.YES_OPTION) {
            saveTicketToFile(passenger, ticket);
        }

    } catch (Exception e) {
        // This will now tell you exactly what went wrong instead of staying silent
        JOptionPane.showMessageDialog(this, "Error building ticket: " + e.getMessage());
    }
    

    }//GEN-LAST:event_btnViewTicketActionPerformed

    private void saveTicketToFile(String passenger, String ticketContent) {
    try {
        // STEP 1: Tell Java to find your Desktop automatically
        String userHome = System.getProperty("user.home");
        
        // STEP 2: Create the full path to the Desktop
        String fileName = userHome + "/Desktop/" + passenger.replaceAll("\\s+", "_") + "_Ticket.txt"; 
        
        // STEP 3: Write the file
        java.io.FileWriter writer = new java.io.FileWriter(fileName);
        writer.write(ticketContent);
        writer.close();
        
        // STEP 4: Success Message
        JOptionPane.showMessageDialog(this, "Ticket saved to your DESKTOP as: " + fileName);
        
    } catch (java.io.IOException e) {
        JOptionPane.showMessageDialog(this, "Print Error: " + e.getMessage());
    }
}

