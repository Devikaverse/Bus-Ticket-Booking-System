private void loadData() {
  DefaultTableModel model = (DefaultTableModel) tblAllBookings.getModel();
    model.setColumnIdentifiers(new Object[]{"ID", "Passenger","Bus No", "Route","Date", "Amount"});
    model.setRowCount(0); 

    try {
        java.sql.Connection con = dao.ConnectionProvider.getCon();
        String query;
        
        // 1. Build the Query
        if (this.userId == 1) { 
            // ADMIN QUERY (No WHERE clause, so NO "?" exists)
            query = "SELECT b.booking_id, b.passenger_name, bus.bus_number, " +
                    "CONCAT(bus.source, ' - ', bus.destination), b.travel_date, b.total_amount " +
                    "FROM bookings b INNER JOIN buses bus ON b.bus_id = bus.bus_id";
        } else {
            // USER QUERY (Has one WHERE clause, so ONE "?" exists)
            query = "SELECT b.booking_id, b.passenger_name, bus.bus_number, " +
                    "CONCAT(bus.source, ' - ', bus.destination), b.travel_date, b.total_amount " +
                    "FROM bookings b INNER JOIN buses bus ON b.bus_id = bus.bus_id " +
                    "WHERE b.user_id = ?";
        }

        java.sql.PreparedStatement ps = con.prepareStatement(query);
        
        // 2. Set the parameter ONLY if we are NOT the admin
        if (this.userId != 1) {
            ps.setInt(1, this.userId); 
        }
        
        // 3. Execute
        java.sql.ResultSet rs = ps.executeQuery();
        
        while(rs.next()) {
            model.addRow(new Object[]{
                rs.getString(1), rs.getString(2), rs.getString(3), 
                rs.getString(4), rs.getString(5), rs.getString(6) 
            });
        }
        
        calculateRevenue();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error in LoadData: " + e.getMessage());
    }

}

private void searchData(String name) {
    DefaultTableModel model = (DefaultTableModel) tblAllBookings.getModel();
    model.setRowCount(0);
    try {
        java.sql.Connection con = dao.ConnectionProvider.getCon();
        String query = "SELECT b.booking_id, b.passenger_name, bus.bus_number, " +
                       "CONCAT(bus.source, ' - ', bus.destination), b.travel_date, b.total_amount " +
                       "FROM bookings b INNER JOIN buses bus ON b.bus_id = bus.bus_id " +
                       "WHERE b.passenger_name LIKE ?";

        java.sql.PreparedStatement ps = con.prepareStatement(query);
        ps.setString(1, "%" + name + "%");

        java.sql.ResultSet rs = ps.executeQuery();
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString(1),
                rs.getString(2),
                rs.getString(3),
                rs.getString(4),
                rs.getString(5),
                rs.getString(6)
            });
        }
        calculateRevenue();
    } catch (Exception e) {
        System.out.println("Search Error: " + e.getMessage());
    }
}

private void calculateRevenue() {
    double total = 0; // Reset to zero every time!
    DefaultTableModel model = (DefaultTableModel) tblAllBookings.getModel();
    
    if (model.getRowCount() == 0) {
        lblTotalRevenue.setText("Total Revenue: ₹0.00");
        return;
    }
    
    for (int i = 0; i < model.getRowCount(); i++) {
        try {
            // We use index 5 because "Amount" is the 6th column
            Object value = model.getValueAt(i, 5);
            if (value != null) {
                String amtStr = value.toString();
                String cleanAmt = amtStr.replaceAll("[^0-9.]", "");
                
                if (!cleanAmt.isEmpty()) {
                    total += Double.parseDouble(cleanAmt);
            }
          }
        } catch (Exception e) {
            System.out.println("Row " + i + " skipped: " + e.getMessage());
        }
        }
    lblTotalRevenue.setText("Total Revenue: ₹" + String.format("%.2f", total));
}

