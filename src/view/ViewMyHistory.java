    private void loadMyBookings() {
        DefaultTableModel model = (DefaultTableModel) tblMyHistory.getModel();
        model.setColumnIdentifiers(new Object[]{"BID", "SID", "Route", "Passenger", "seats", "Total Paid", "Travel Date"});
        model.setRowCount(0);
        try {
            Connection con = ConnectionProvider.getCon();
            String query = "SELECT booking_id, schedule_id, route, passenger_name, seats_booked, total_amount, travel_date "
                         + "FROM bookings WHERE user_id = ?";

            PreparedStatement ps = con.prepareStatement(query);
            ps.setInt(1, this.userId);
            ResultSet rs = ps.executeQuery();
       
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt(1),    // BID
                    rs.getInt(2),    // SID
                    rs.getString(3), // Route
                    rs.getString(4), // Passenger
                    rs.getInt(5),    // Seats
                    rs.getDouble(6), // Total Paid
                    rs.getString(7)  // Travel Date
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
          
