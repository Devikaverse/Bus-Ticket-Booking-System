    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
    int row = tblMyHistory.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a booking to cancel!");
            return;
        }

        try {
            String dateStr = tblMyHistory.getValueAt(row, 6).toString().trim();
            LocalDate travelDate = LocalDate.parse(dateStr);
            LocalDate today = LocalDate.now();

            if (travelDate.isBefore(today)) {
                JOptionPane.showMessageDialog(this, "You cannot cancel a past trip!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to cancel?", "Confirm", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                String bId = tblMyHistory.getValueAt(row, 0).toString();
                String sId = tblMyHistory.getValueAt(row, 1).toString();
                // Get seat count for math logic
                int seatsToReturn = Integer.parseInt(tblMyHistory.getValueAt(row, 4).toString());

                Connection con = ConnectionProvider.getCon();
                con.setAutoCommit(false);

                PreparedStatement ps1 = con.prepareStatement("DELETE FROM bookings WHERE booking_id = ?");
                ps1.setString(1, bId);
                ps1.executeUpdate();

                // Logic: Add the correct number of seats back to the schedule
                PreparedStatement ps2 = con.prepareStatement("UPDATE schedules SET available_seats = available_seats + ? WHERE schedule_id = ?");
                ps2.setInt(1, seatsToReturn);
                ps2.setString(2, sId);
                ps2.executeUpdate();

                con.commit();
                JOptionPane.showMessageDialog(this, "Cancelled! Your seats are now available.");
                loadMyBookings();
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCancelActionPerformed


