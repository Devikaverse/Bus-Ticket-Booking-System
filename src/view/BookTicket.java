    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
String source = txtSearchSource.getText().trim();
    String dest = txtSearchDest.getText().trim();
    
    if (jDateChooser1.getDate() == null) {
        JOptionPane.showMessageDialog(null, "Please select a travel date!");
        return; 
    }

    java.text.SimpleDateFormat dFormat = new java.text.SimpleDateFormat("yyyy-MM-dd");
    String travelDate = dFormat.format(jDateChooser1.getDate());

    DefaultTableModel model = (DefaultTableModel) tblBuses.getModel();
    model.setRowCount(0); 

    try {
        java.sql.Connection con = dao.ConnectionProvider.getCon();
        
        // Use JOIN to get bus details and schedule details together
         // This SQL now searches for a 4-day window (Selected Date minus 1, plus 2)
           String query = "SELECT s.schedule_id, s.bus_id, b.bus_number, b.source, b.destination, s.fare, s.travel_date " +
                         "FROM schedules s JOIN buses b ON s.bus_id = b.bus_id " +
                         "WHERE LOWER(b.source) = LOWER(?) " + 
                         "AND LOWER(b.destination) = LOWER(?) " + 
                         "AND s.travel_date BETWEEN DATE_SUB(?, INTERVAL 1 DAY) AND DATE_ADD(?, INTERVAL 2 DAY) " +
                         "ORDER BY s.travel_date ASC";

        java.sql.PreparedStatement ps = con.prepareStatement(query);
        
        // FIX: Remove the "%" symbols. Use exact matches for source and dest.
        ps.setString(1, source);
        ps.setString(2, dest);
        ps.setString(3, travelDate);
        ps.setString(4, travelDate);
        
        java.sql.ResultSet rs = ps.executeQuery();
        
        boolean found = false;
        while(rs.next()) {
            found = true;
            model.addRow(new Object[]{
                rs.getInt("schedule_id"), // Index 0 (Hidden)
                rs.getInt("bus_id"),      // Index 1
                rs.getString("bus_number"),
                rs.getString("source"),
                rs.getString("destination"),
                rs.getDouble("fare"),
                rs.getString("travel_date")
            });
        }
        
        if (!found) {
            JOptionPane.showMessageDialog(null, "No buses found for " + source + " to " + dest + " on " + travelDate);
        }
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Search Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSearchActionPerformed

