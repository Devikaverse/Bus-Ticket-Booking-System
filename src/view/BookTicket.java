    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
      int row = tblBuses.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(null, "Please select a bus from the table!");
        return;
    }

    try {
        // We take the IDs directly from the table (Hidden columns 0 and 1)
        int scheduleId = Integer.parseInt(tblBuses.getValueAt(row, 0).toString());
        int busId = Integer.parseInt(tblBuses.getValueAt(row, 1).toString());
        
        // Get the strings for the next window
        String travelDate = tblBuses.getValueAt(row, 6).toString();
        String route = tblBuses.getValueAt(row, 3).toString() + " - " + tblBuses.getValueAt(row, 4).toString();

        // Pass everything to SeatSelection
        new view.SeatSelection(scheduleId, this.userId, travelDate, busId, route).setVisible(true);
        
        this.dispose();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Selection Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnConfirmActionPerformed

