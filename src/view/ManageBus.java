    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String busNum = txtBusNumber.getText().trim();
    String source = txtSource.getText().trim();
    String destination = txtDestination.getText().trim();
    String fare = txtFare.getText().trim();
    if (!busNum.matches("^[A-Z]{2}-\\d{2}-[A-Z]{2}-\\d{4}$")) {
    JOptionPane.showMessageDialog(null, "Please use format: AP-31-TV-1234");
    return;
    }
    String tDate = "";
    if (jDateChooser1.getDate() != null) {
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
        tDate = sdf.format(jDateChooser1.getDate());
    }
    // ---------------------------------

    String type = "";
    if(rbAC.isSelected()) type = "AC";
    else if(rbNonAC.isSelected()) type = "Non-AC";

    if(busNum.isEmpty() || source.isEmpty() || destination.isEmpty() || fare.isEmpty() || tDate.isEmpty() || type.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please fill all fields and select a Bus Type!");
        return;
    }
    // Logic to ensure a type is picked
    
    try {
        java.sql.Connection con = dao.ConnectionProvider.getCon();
        String query = "INSERT INTO buses (bus_number, source, destination, fare, bus_type, travel_date) VALUES (?, ?, ?, ?, ?, ?)";
        java.sql.PreparedStatement ps = con.prepareStatement(query);
        
        ps.setString(1, busNum);
        ps.setString(2, source);
        ps.setString(3, destination);
        ps.setDouble(4, Double.parseDouble(fare)); 
        ps.setString(5, type);
        ps.setString(6, tDate);
        
        ps.executeUpdate();
        JOptionPane.showMessageDialog(null, "Bus " + busNum + " added successfully for " + tDate);
        
        loadAllBuses();
        
        // Clear fields after success
        txtBusNumber.setText("");
        txtSource.setText("");
        txtDestination.setText("");
        txtFare.setText("");
        jDateChooser1.setDate(null);
        rbAC.setSelected(true);
        txtSource.setText("");
        txtDestination.setText("");
        // busTypeGroup.clearSelection(); // Optional
        
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Fare must be a valid number!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnSaveActionPerformed

