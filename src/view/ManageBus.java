public void loadAllBuses() {
    DefaultTableModel model = (DefaultTableModel) tblBuses.getModel();
    model.setRowCount(0);
    try {
        Connection con = dao.ConnectionProvider.getCon();
        PreparedStatement ps = con.prepareStatement(
            "SELECT bus_id, bus_number, source, destination, fare, bus_type, travel_date FROM buses"
        );
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("bus_id"),
                rs.getString("bus_number"),
                rs.getString("source"),
                rs.getString("destination"),
                rs.getDouble("fare"),
                rs.getString("bus_type"),
                rs.getString("travel_date")
            });
        }
        // Force the table to visually refresh
        tblBuses.revalidate();
        tblBuses.repaint();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Load Error: " + e.getMessage());
    }
}
