private void loadData() {
    
    DefaultTableModel model = (DefaultTableModel) tblSchedules.getModel();
    // Make sure this line is at the top of loadData()
    model.setColumnIdentifiers(new Object[]{"ID", "Bus ID", "Travel Date", "Seats", "Fare"});
    model.setRowCount(0); // Clear the table first

    try {
        java.sql.Connection con = dao.ConnectionProvider.getCon();
        java.sql.Statement st = con.createStatement();
        // Make sure your table name in MySQL is 'schedules'
        java.sql.ResultSet rs = st.executeQuery("SELECT * FROM schedules");

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("schedule_id"),
                rs.getString("bus_id"),
                rs.getString("travel_date"),
                rs.getString("available_seats"),
                rs.getString("fare")
            });
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(null, "Error loading schedules: " + e.getMessage());
    }
}
