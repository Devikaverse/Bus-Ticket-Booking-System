private void loadBusesToCombo() {
        try {
            Connection con = dao.ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT bus_id, bus_number FROM buses");
            comboBuses.removeAllItems(); 
            while(rs.next()) {
                comboBuses.addItem(rs.getString("bus_id") + " - " + rs.getString("bus_number"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Database Error: " + e.getMessage());
        }
    }
    
    private void loadScheduleTable() {
        DefaultTableModel model = (DefaultTableModel) tblSchedules.getModel();
    model.setRowCount(0); 
    // Match the columns used in your loadData/Delete logic
    model.setColumnIdentifiers(new String[]{"ID", "Bus No", "Date", "Fare", "Seats"});
    try {
        Connection con = dao.ConnectionProvider.getCon();
        String query = "SELECT s.schedule_id, b.bus_number, s.travel_date, s.fare, s.available_seats " +
                       "FROM schedules s JOIN buses b ON s.bus_id = b.bus_id";
        ResultSet rs = con.createStatement().executeQuery(query);
        while(rs.next()) {
            model.addRow(new Object[]{
                rs.getString("schedule_id"), 
                rs.getString("bus_number"), 
                rs.getString("travel_date"), 
                rs.getString("fare"),
                rs.getString("available_seats")
            });
        }
    } catch (Exception e) {
        System.out.println("Table Load Error: " + e.getMessage());
    }
    }
