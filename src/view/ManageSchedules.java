    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
            String busId = comboBuses.getSelectedItem().toString().split(" - ")[0];
            String date = new SimpleDateFormat("yyyy-MM-dd").format(dateChooser.getDate());
            double fare = Double.parseDouble(txtFare.getText());
            
            Connection con = dao.ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("INSERT INTO schedules (bus_id, travel_date, available_seats, fare) VALUES (?, ?, 40, ?)");
            ps.setString(1, busId);
            ps.setString(2, date);
            ps.setDouble(3, fare);
            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Successfully Added!");
            loadScheduleTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAddActionPerformed

