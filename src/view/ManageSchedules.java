    private void btnDeleteSelectedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteSelectedActionPerformed
        // TODO add your handling code here:
        // 1. Get the selected row index
    int selectedRow = tblSchedules.getSelectedRow(); 
    
    // 2. Check if a row is actually selected
    if(selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a schedule from the table first!");
        return;
    }

    // 3. Get the ID from the first column (Index 0)
    // Make sure your table's first column is the Schedule ID
    String scheduleId = tblSchedules.getValueAt(selectedRow, 0).toString();
    
    // 4. Confirmation Popup
    int confirm = JOptionPane.showConfirmDialog(this, 
        "Are you sure you want to delete Schedule ID: " + scheduleId + "?", 
        "Confirm Deletion", JOptionPane.YES_NO_OPTION);
        
    if(confirm == JOptionPane.YES_OPTION) {
        try {
            java.sql.Connection con = dao.ConnectionProvider.getCon();
            // Using a Prepared Statement to prevent SQL injection
            java.sql.PreparedStatement ps = con.prepareStatement("DELETE FROM schedules WHERE schedule_id = ?");
            ps.setString(1, scheduleId);
            
            int result = ps.executeUpdate();
            
            if(result > 0) {
                JOptionPane.showMessageDialog(this, "Schedule deleted successfully!");
                loadData(); // This refreshes your table so the deleted row disappears
            } else {
                JOptionPane.showMessageDialog(this, "Error: Schedule ID not found in database.");
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnDeleteSelectedActionPerformed

